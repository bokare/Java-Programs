package com.company;

public class numSystem_conversion {
    public static void main(String[] args) {
        // input format = octal ; octal numbers are written by placing 0 before numbers.
        int m= 0030 ;
        int v =0070;
        System.out.println(m);
        System.out.println(v);
        System.out.println(011);


        // decimal
        int b =12 ;
        System.out.println(b);


        // input format = hexadecimal  ;  hexadecimal numbers are written by placing 0x before numbers.
        int hexD = 0xd ;
        System.out.println(0xa);  // 10
        System.out.println(hexD);  // 13
        System.out.println(0xab);
    }
}
